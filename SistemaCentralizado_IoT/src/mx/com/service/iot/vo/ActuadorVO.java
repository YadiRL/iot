package mx.com.service.iot.vo;

public class ActuadorVO {
	
	private int idActuador;
	private String nombreActuador;
	private int estado;
	public int getIdActuador() {
		return idActuador;
	}
	public void setIdActuador(int idActuador) {
		this.idActuador = idActuador;
	}
	public String getNombreActuador() {
		return nombreActuador;
	}
	public void setNombreActuador(String nombreActuador) {
		this.nombreActuador = nombreActuador;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}

	
	
	

}
